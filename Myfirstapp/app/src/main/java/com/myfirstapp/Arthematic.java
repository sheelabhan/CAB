package com.myfirstapp;

public class Arthematic {
    private int num1;
    private int num2;
    //constructor
    public Arthematic(int first, int second){
        num1=first;
        num2=second;

    }

    public int add(){
        return num1+num2;
    }
    public int multiply(){

        return num1*num2;
    }
}
